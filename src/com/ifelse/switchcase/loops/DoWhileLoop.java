package com.ifelse.switchcase.loops;

public class DoWhileLoop {

	public static void main(String[] args) {

		int i = 1;
		do {
			System.out.println(i);
		}
		while(i >= 10);
		/*
		int d = 1;
		do {
			System.out.println(d);
		}
		while (d <= 1);
		*/
		int d = 1;
		do {
			System.out.println(d);
			d++;
		}
		while (d <= 10);

	}

}
